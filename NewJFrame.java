    public static ArrayList<Team> rotate(ArrayList<Team> old){
        ArrayList<Team> newRotation = new ArrayList<>();
        newRotation.add(old.get(0));
        for(int i=1; i<old.size(); i++){
            if(i+1>=old.size()){
                newRotation.add(old.get(1));
            }
            else {
                newRotation.add(old.get(i + 1));
            }
        }
        return newRotation;
    }
